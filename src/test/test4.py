def getSameItemFirst(nlist):
    """
    判断二维数组下列表完全相等方法一：判断相同位置处元素相等
    :param nlist:
    :return:
    """
    tmp_dict = {}
    rest = {}
    n = 0
    for item in nlist:
        tmp_list = []
        for k, v in item.items():
            n += 1
            x_key = 'B' + str(n)
            if not tmp_dict:
                tmp_dict[x_key] = v
                tmp_list.append(k)
                rest[x_key] = tmp_list
            else:
                v_key_list, v_value_list = [], []
                for v_key, v_value in tmp_dict.items():
                    v_key_list.append(v_key)
                    v_value_list.append(v_value)

                vlsit = list(zip(v_key_list, v_value_list))
                for v_item in vlsit:
                    if v_item[1] == v:
                        rest[v_item[0]].append(k)
                    else:
                        if v not in tmp_dict.values():
                            tmp_dict[x_key] = v
                            tmp_list.append(k)
                            rest[x_key] = tmp_list

    print(tmp_dict)
    print("******" * 3)
    print(rest)
    return  tmp_dict, rest




